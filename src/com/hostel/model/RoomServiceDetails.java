package com.hostel.model;

public class RoomServiceDetails {
	private String uni_roll;
	private String room_no;
	private String service;
	private String availability_time;
	private String hostel;
	private String ref_no;
	
	public String getRef_no() {
		return ref_no;
	}
	public void setRef_no(String ref_no) {
		this.ref_no = ref_no;
	}
	public String getUni_roll() {
		return uni_roll;
	}
	public void setUni_roll(String uni_roll) {
		this.uni_roll = uni_roll;
	}
	public String getRoom_no() {
		return room_no;
	}
	public void setRoom_no(String room_no) {
		this.room_no = room_no;
	}
	public String getService() {
		return service;
	}
	public void setService(String service) {
		this.service = service;
	}
	public String getAvailability_time() {
		return availability_time;
	}
	public void setAvailability_time(String availability_time) {
		this.availability_time = availability_time;
	}
	public String getHostel() {
		return hostel;
	}
	public void setHostel(String hostel) {
		this.hostel = hostel;
	}
	
}
